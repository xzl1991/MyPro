package com.huoli.bmall.model;

public class Province
{
	private int id;
	private String name;
	private int pid;
	private int hbid;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public int getPid()
	{
		return pid;
	}

	public void setPid(int pid)
	{
		this.pid = pid;
	}

	public int getHbid()
	{
		return hbid;
	}

	public void setHbid(int hbid)
	{
		this.hbid = hbid;
	}

}
